$mulopimfwc_location_archive_get_filter_area = static function () {
    $filter_area_callbacks = apply_filters('mulopimfwc_location_archive_filter_area_callbacks', []);

    foreach ((array) $filter_area_callbacks as $filter_area_callback) {
        if (!is_callable($filter_area_callback)) {
            continue;
        }

        ob_start();
        $filter_area_result = call_user_func($filter_area_callback);
        $filter_area_html = trim(ob_get_clean());

        if (is_string($filter_area_result)) {
            $filter_area_html .= trim($filter_area_result);
        }

        if ($filter_area_html !== '') {
            return $filter_area_html;
        }
    }

    $filter_sidebar_ids = apply_filters('mulopimfwc_location_archive_filter_sidebar_ids', [
        'filters-area',
        'shop-filters',
        'shop-filters-area',
        'shop_filters',
        'shop_filters_area',
        'woocommerce-filters',
        'woocommerce_filters',
    ]);

    foreach ((array) $filter_sidebar_ids as $filter_sidebar_id) {
        $filter_sidebar_id = is_string($filter_sidebar_id) ? trim($filter_sidebar_id) : '';

        if ($filter_sidebar_id === '' || !is_active_sidebar($filter_sidebar_id)) {
            continue;
        }

        ob_start();
        dynamic_sidebar($filter_sidebar_id);
        $filter_widgets_html = trim(ob_get_clean());

        if ($filter_widgets_html === '') {
            continue;
        }

        ob_start();
        ?>
        <div class="filters-area" style="display: none;">
            <div class="filters-inner-area wd-grid-g" style="--wd-col-lg:6;--wd-col-md:2;--wd-col-sm:1;--wd-gap-lg:30px;--wd-gap-sm:20px;">
                <?php
                // phpcs:ignore WordPress.Security.EscapeOutput.OutputNotEscaped -- Widget markup is generated by WordPress/theme callbacks.
                echo $filter_widgets_html;
                ?>
            </div>
        </div>
        <?php

        $filter_area_html = trim(ob_get_clean());

        return apply_filters('mulopimfwc_location_archive_filter_area_html', $filter_area_html, $filter_sidebar_id);
    }

    return '';
};

$mulopimfwc_location_archive_render_before_shop_loop = static function () use ($mulopimfwc_location_archive_get_filter_area) {
    ob_start();
    do_action('woocommerce_before_shop_loop');
    $before_shop_loop_html = trim(ob_get_clean());

    if ($before_shop_loop_html !== '') {
        // phpcs:ignore WordPress.Security.EscapeOutput.OutputNotEscaped -- WooCommerce/theme callbacks render trusted shop-loop markup.
        echo $before_shop_loop_html;
    }

    $needs_filter_area = $before_shop_loop_html !== ''
        && false !== strpos($before_shop_loop_html, 'open-filters')
        && false === strpos($before_shop_loop_html, 'filters-area');

    $needs_filter_area = (bool) apply_filters(
        'mulopimfwc_location_archive_should_render_filter_area',
        $needs_filter_area,
        $before_shop_loop_html
    );

    if (!$needs_filter_area) {
        return;
    }

    $filter_area_html = $mulopimfwc_location_archive_get_filter_area();

    if ($filter_area_html === '') {
        return;
    }

    // phpcs:ignore WordPress.Security.EscapeOutput.OutputNotEscaped -- Filter area markup is generated by WordPress/theme callbacks.
    echo $filter_area_html;
};

