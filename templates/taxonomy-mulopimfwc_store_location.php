$mulopimfwc_location_archive_enqueue_registered_assets = static function ($handles, $type) {
    foreach ((array) $handles as $handle) {
        $handle = is_string($handle) ? trim($handle) : '';

        if ($handle === '') {
            continue;
        }

        if ($type === 'style') {
            if (wp_style_is($handle, 'registered') || wp_style_is($handle, 'enqueued')) {
                wp_enqueue_style($handle);
            }

            continue;
        }

        if (wp_script_is($handle, 'registered') || wp_script_is($handle, 'enqueued')) {
            wp_enqueue_script($handle);
        }
    }
};

add_action(
    'wp_enqueue_scripts',
    static function () use ($mulopimfwc_location_archive_enqueue_registered_assets) {
        if (!apply_filters('mulopimfwc_location_archive_enqueue_theme_filter_assets', true)) {
            return;
        }

        $mulopimfwc_location_archive_enqueue_registered_assets(
            apply_filters('mulopimfwc_location_archive_filter_script_handles', [
                'wd-filters-area',
            ]),
            'script'
        );

        $mulopimfwc_location_archive_enqueue_registered_assets(
            apply_filters('mulopimfwc_location_archive_filter_style_handles', [
                'wd-shop-filter-area',
                'wd-widget-price-filter',
                'wd-widget-layered-nav-stock-status',
                'wd-widget-wd-layered-nav',
                'wd-widget-product-cat',
                'wd-select2',
            ]),
            'style'
        );
    },
    1000
);

